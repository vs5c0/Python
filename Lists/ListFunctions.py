
Lucky_numbers = [1, 45, 6, 7, 3, 3, 634, 2]
friends = ["Kiran", "Ram", "Vamsi", "Pavan", "Hyder", "Ram"]
print(friends)

# Extend Method in List
friends.extend(Lucky_numbers)
print(friends)

# Append Method in List
friends.append("Oscar")
print(friends)

# Insert Method in List
friends.insert(3, "Vikram")
print(friends)

# Count Method in List
friends.count("Lucky_numbers")
print(friends)

# Pop Method in List
friends.pop(2)
print(friends)


# reverse Method in List
friends.reverse()
print(friends)

# Copy Method in List
friends2 = friends.copy()
print(friends2)



# Remove Method in List
friends.remove("Pavan")
print(friends)

# Clear Method in List
friends.clear()
print(friends)


